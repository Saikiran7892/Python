#avg of two subjects marks
'''
step1:remember first subject marks
step2:remember second subject marks
step3:calculate Total_marks
step4:avg=total/2
step5:show me the output'''


subject1_marks=int(input('Enter first subject marks:'))#20
subject2_marks=int(input('Enter second subject marks:'))#30
total_marks=subject1_marks+subject2_marks#20+30=50
avg_marks=total_marks/2#50/2=25.0
print('Avg of two subject marks:',avg_marks)
